        <h2>Article of 03/25/2019 (Non-working PEKA card)</h2>
        <article>
            <section class="buttons" id="events">
                <a href="../docs/artGW_25-03-2019.pdf" target="_blank" class="button">Article in Głos Wielkopolski, dated March 25, 2019 (text: Agnieszka Mozolewska)</a>
            </section>
            <b>Own comment:</b>
            <p>The whole of the above matter could be described in a maximum of 3 sentences. However, Głos Wielkopolski, as is his habit, can do BIG THING even with such simple things. I will describe in these 3 sentences what the problem was. During the inspection, the passenger probably had a defective PEKA card, which could not be confirmed in any way by the inspector, and therefore issued an additional fee. The passenger at the nearest ZTM point reported the failure of her card, for which she received the document mentioned in the article by Głos Wielkopolski, and which was not a document issued by our controller. Then, for a period of 3 months from the date of issuing the additional fee, the passenger did not provide our company with any document confirming the inoperability of their card. And that's it. Thus, despite the fact that the passenger has 3 months to lodge an appeal-complaint against the imposed fee, the passenger in this case did not take care of her own matters and did not receive a letter from the ZTM confirming the inoperability of her card at the time of the ticket inspection. The press article, however, lacks even good advice for the passenger and other passengers, so that they take better care of their own affairs and, however, if they have documents confirming the inoperability of e.g. PEKA cards, it is better to attach them to their appeals. There would then be no problem with the cancellation of the additional fee. I will say more, even if the complaining lady provided such a document after the 3-month deadline, but she would show that she asked the ZTM for issuing one within 3 months, we would also take into account such a complaint. But we are not able to do it for passengers. These steps have to be done by those concerned personally.</p>
