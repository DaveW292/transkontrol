            <h3>Uwagi do powyższych incydentów.</h3>
            <section>
                <p>Możliwymi konsekwencjami naruszenia może być nieuprawnione wykorzystanie danych osobowych m.in. w celu</p>
                <ol>
                    <li>uzyskania przez osoby trzecie, na szkodę osoby, której dane naruszono, kredytów w instytucjach poza bankowych ponieważ wiele takich instytucji umożliwia uzyskanie pożyczki lub kredytu w łatwy i szybki sposób np. przez Internet lub telefonicznie bez konieczności okazywania dokumentu tożsamości</li>
                    <li>uzyskania dostępu do korzystania ze świadczeń opieki zdrowotnej przysługujących osobie, której dane naruszono oraz do jej danych o stanie zdrowia, ponieważ często dostęp do systemów rejestracji pacjenta można uzyskać telefonicznie potwierdzając swoją tożsamość za pomocą numeru PESEL</li>
                    <li>korzystania z praw obywatelskich osoby, której dane naruszono, np.: do głosowania nad środkami budżetu obywatelskiego. Uniemożliwiałoby to właściwej osobie skorzystanie z przysługującego jej prawa</li>
                    <li>wyłudzenia ubezpieczenia lub środków z ubezpieczenia, co może spowodować dla osoby, której dane dotyczą, negatywne konsekwencje w postaci problemów związanych z próbą przypisania jej odpowiedzialności za dokonanie takiego oszustwa</li>
                    <li>zawarcia umowy o świadczenie usług, tj. np. telewizja kablowa, telefon, Internet a następnie zaprzestanie opłacania rachunków i spowodowanie dla osoby, której dane dotyczą, negatywnych konsekwencji w postaci zadłużenia</li>
                </ol>
