            <h3>TOLERANCJA</h3>
            <p>Firma nasza stosuje zasadę tolerancji i poszanowania odmienności poglądów, zachowań, czy też wyglądu osób. Dlatego też do pracy w charakterze kontrolerów biletowych przyjmujemy wszystkie osoby, bez względu na swoje ułomności w wyglądzie czy też bez względu na wiek. Najważniejszym dla nas jest chęć do pracy, zaangażowanie i odpowiednie predyspozycje oraz stan zdrowia umożliwiający wykonywanie tej pracy.</p>
            <p>Postawa taka powoduje niestety, że wielu osobom nie podoba się to, iż kontroler jest starszy, albo ma twarz taką a nie inną. Proszę jednak pamiętać, że nie każdy może być przystojny, ładny lub odpowiadać stopniem "piękności" wszystkim pasażerom. Nie każdy również jest młody. Jednak wszystkie takie osoby również potrzebują zaistnieć w społeczeństwie oraz muszą pracować, jeśli tego chcą.</p>
            <p>Dlatego nasza firma jest tolerancyjna, nie dyskryminujemy ze względu na wiek, wygląd, stan zdrowia (przy braku przeciwwskazań) czy też podobne cechy. I może wydaje się to dziwne, iż o tym piszemy, albowiem postawa taka powinna być powszechnie stosowana, jednak w praktyce spotykamy się często z wulgaryzmami kierowanymi w stronę takich osób. Kontrolowane osoby niestety również wykazują swoją niechęć wobec takich pracowników.</p>
            <p>Nie ma znaczenia również dla nas wykształcenie. Dlaczego bowiem osoby z wykształceniem niższym niż średnie nie mogą być dobrymi kontrolerami biletowymi?</p>
            <p>Tak naprawdę to czy osoba nadaje się na kontrolera czy też nie, zależy tylko od indywidualnych predyspozycji każdego człowieka, a nie od wykształcenia, wyglądu czy też wieku. Dlatego każdej osobie dajemy szansę i wyznaczamy okres próbny. Najczęściej już podczas wstępnych szkoleń przekonujemy się, czy dany kandydat podoła tej pracy. Również jednak i tego typu postawa wielokrotnie źle jest odbierana przez dużą grupę społeczeństwa.</p>
            <p>Nam jednak wszystkie opisane wyżej i negatywne postawy są obce, a najważniejsze dla nas jest to, aby kontroler posiadał niezbędne cechy.</p>
